var A = [
    [ 2,  5, 8, 10],
    [ 4,  2, 4,  5],
    [ 3, -1, 3,  2],
    [-8, -2, 4,  5]
];

var B = [
    [ 1,  3, -8,  2],
    [ 3,  1,  4, 10],
    [-1,  2, -3, 12],
    [ 5, -3,  3,  8]
];

var C = [];

for (i = 0; i < A.length; i++) {
    C[i] = [];
    for (j = 0; j < A[i].length; j++) {
        C[i][j] = A[i][j] + B[i][j];
    }
}

console.log(C);
